namespace Vibe.UI.Enums;

/// <summary>
/// Defines the size options for components.
/// Used across multiple components to provide consistent sizing.
/// </summary>
public enum ComponentSize
{
    /// <summary>
    /// Small component size.
    /// Provides a compact appearance suitable for dense layouts.
    /// </summary>
    Small,

    /// <summary>
    /// Medium component size.
    /// The default size for most components, balancing space and usability.
    /// </summary>
    Medium,

    /// <summary>
    /// Large component size.
    /// Provides a more prominent appearance with increased spacing and padding.
    /// </summary>
    Large
}
