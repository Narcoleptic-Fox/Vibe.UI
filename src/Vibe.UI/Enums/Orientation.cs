namespace Vibe.UI.Enums;

/// <summary>
/// Defines the orientation options for layout and directional components.
/// Used to control the flow direction of elements within containers or groups.
/// </summary>
public enum Orientation
{
    /// <summary>
    /// Horizontal orientation.
    /// Elements are arranged in a left-to-right or right-to-left flow.
    /// Commonly used for toolbars, tabs, and horizontal navigation menus.
    /// </summary>
    Horizontal,

    /// <summary>
    /// Vertical orientation.
    /// Elements are arranged in a top-to-bottom flow.
    /// Commonly used for sidebars, vertical menus, and stacked layouts.
    /// </summary>
    Vertical
}
